'Это пятничный вечер. Вы просто устали. Каждый день много работы и постоянный стресс. Вы и ваш собеседник или ваша собеседница хотите что-то предпринять на выходных.';

'Задача:';
'Совместно с вашим собеседником или собеседницей запланировать, что можно сделать.';

'Вот несколько заметок:';

'1) Когда?';
'2) Куда?';
'3) Что делать?';
'4) Кто присоединится?';
'5) Есть ли еще идеи на выходные?';

