Klaus: 'Am Sonntag schlafen wir aus. Dann will ich zum Fußball mit Peter und du kannst mit Lisa etwas machen. Am Abend können wir dann miteinander kochen und können wir früh schlafen. Wir müssen auch am Montag sehr früh zur Arbeit.';
